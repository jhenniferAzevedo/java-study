package maratonajava.introducao;

public class Operators {
    public static void main(String[] args) {

        // arithmetic: + - * /
        int num1 = 10, num2 = 20;
        System.out.println(num1 + num2 + " string " + num1 + num2);

        // relational: < > <= >= == !=
        boolean isTen = 10.00 == 10;
        System.out.println(isTen);

        // logic: &&, || and !
        // assignment: = += *=
    }
}
